    private void checkOutBreadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_checkOutBreadMouseClicked
        // TODO add your handling code here:
        
        int cake, past, iced, hot;
        for(int i = 0; i < 35; i++)
        {
            if(quantity[i] > 0)
            {
                if(i >= 0 && i <= 6)
                {
                    food smentara = menubread.getmenu().get(i);
                    orderan.setPorder(smentara);
                }
                if(i >= 7 && i <= 13)
                {
                    cake = i - 7;
                    food smentara = menucake.getmenu().get(cake);
                    orderan.setPorder(smentara);
                }
                if(i >= 14 && i <= 20)
                {
                    past = i - 14;
                    food smentara = menupast.getmenu().get(past);
                    orderan.setPorder(smentara);
                }
                if(i >= 21 && i <= 27)
                {
                    iced = i - 21;
                    drink smentara = menuice.getmenu().get(iced);
                    orderan.setOorder(smentara);
                }
                if(i >= 28 && i <= 34)
                {
                    hot = i - 28;
                    drink smentara = menuhot.getmenu().get(hot);
                    orderan.setOorder(smentara);
                }
            }
        }
        
        int counter = orderan.getPorder().size();
        int counter1 = orderan.getOorder().size();
        
        try{
            //replace directory with receipt directory
            FileWriter cetak = new FileWriter("receipt.txt");
            cetak.write(custName + "|\n");
            for(int i = 0 ; i < counter; i++){
                cetak.write(orderan.getPorder().get(i).getname() + 
                        "|" + orderan.getPorder().get(i).getprice() + "|\n");
            }
            
            for(int i = 0 ; i < counter1; i++){
                cetak.write(orderan.getOorder().get(i).getname() + 
                        "|" + orderan.getOorder().get(i).getprice()+ "|\n");
            }
            cetak.write("Rp.|" + subTotal + "|\n");
            cetak.close();
            System.out.println("Receipt generated.");
        }
        catch(IOException e){
            System.out.println("An Error occured.");
        }
        new Receipt().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_checkOutBreadMouseClicked

